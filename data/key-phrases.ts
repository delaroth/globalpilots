/**
 * Get the phrasebook for a country.
 * Returns null for English-speaking countries (no phrasebook needed).
 * @param countryCode - ISO 3166-1 alpha-2 code (e.g. "TH", "JP")
 * @returns LanguagePhrases or null if country not found or language is English
 */
export function getPhrasebook(countryCode: string): LanguagePhrases | null {
  const code = countryCode.toUpperCase()
  const language = COUNTRY_LANGUAGE_MAP[code]

  if (!language || language === 'English') return null

  return PHRASE_SETS[language] ?? null
}

/**
 * Get climate data for a destination by IATA code and month number (1-12).
 */
export function getClimateData(cityCode: string, month: number): ClimateData | null {
  const code = cityCode.toUpperCase()
  const data = climateDb[code]
  if (!data) return null

  const monthIndex = month - 1
  if (monthIndex < 0 || monthIndex > 11) return null

  return toClimate(fromCompact(data)[monthIndex])
}

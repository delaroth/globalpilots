// Hardcoded climate normals for all destinations by month (1-12)
// Data sourced from climate-data.org / weatherspark.com averages
// This is more reliable than API calls since climate normals rarely change

export interface ClimateData {
  avgTempC: number
  avgTempF: number
  rainyDays: number
  humidity: number
  description: string
  packingTip: string
}

interface MonthlyClimate {
  tempC: number
  rainyDays: number
  humidity: number
  desc: string
  tip: string
}

// Helper to build ClimateData from compact format
function toClimate(m: MonthlyClimate): ClimateData {
  return {
    avgTempC: m.tempC,
    avgTempF: Math.round(m.tempC * 9 / 5 + 32),
    rainyDays: m.rainyDays,
    humidity: m.humidity,
    description: m.desc,
    packingTip: m.tip,
  }
}

// Compact monthly data: [tempC, rainyDays, humidity, description, packingTip]
// Indexed by month 0-11 (Jan=0, Dec=11)
type CompactMonth = [number, number, number, string, string]

function fromCompact(data: CompactMonth[]): MonthlyClimate[] {
  return data.map(([tempC, rainyDays, humidity, desc, tip]) => ({
    tempC, rainyDays, humidity, desc, tip,
  }))
}

